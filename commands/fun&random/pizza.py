class PizzaView(View):
    """Vue contenant un bouton pour rÃ©gÃ©nÃ©rer une pizza alÃ©atoire."""
    def __init__(self, data: dict, author: discord.User | discord.Member):
        super().__init__(timeout=60)
        self.data = data
        self.author = author
        self.message: discord.Message | None = None

    async def on_timeout(self):
        """DÃ©sactive les boutons Ã  la fin du timeout."""
        for child in self.children:
            child.disabled = True
        if self.message:
            try:
                await safe_edit(self.message, view=self)
            except Exception:
                pass

    @button(label="ğŸ• Nouvelle pizza", style=discord.ButtonStyle.green)
    async def nouvelle_pizza(self, interaction: discord.Interaction, button: discord.ui.Button):
        """RÃ©gÃ©nÃ¨re une pizza alÃ©atoire (seul lâ€™auteur peut le faire)."""
        if interaction.user != self.author:
            return await safe_interact(interaction, content="âŒ Ce n'est pas ta pizza !", ephemeral=True)

        new_embed = generate_pizza_embed(self.data)
        await safe_interact(interaction, edit=True, embed=new_embed, view=self)

