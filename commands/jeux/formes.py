class MemoryButton(discord.ui.Button):
    def __init__(self, symbol, color, sequence, user_id):
        super().__init__(label=symbol, style=discord.ButtonStyle.secondary)
        self.symbol = symbol
        self.color = color
        self.sequence = sequence
        self.user_id = user_id
        self.user_sequence = []

