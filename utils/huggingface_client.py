def get_story_continuation(history: list[dict]) -> str:
    """
    G√©n√®re la suite du sc√©nario √† partir de l'historique.
    Chaque √©l√©ment de 'history' est une dict : {role: 'user'/'assistant'/'system', content: str}
    """

    # On recompose le prompt complet √† partir de l'historique
    prompt = "\n".join([f"{m['role']}: {m['content']}" for m in history])
    payload = {
        "inputs": f"{prompt}\nassistant:",
        "parameters": {
            "max_new_tokens": 250,
            "temperature": 0.9,
            "repetition_penalty": 1.1,
        }
    }

    try:
        response = requests.post(MODEL_URL, headers=HEADERS, json=payload, timeout=60)

        if response.status_code != 200:
            print(f"[Erreur HuggingFace] {response.status_code}: {response.text}")
            return "‚ö†Ô∏è Le narrateur reste silencieux... (*erreur du mod√®le*)"

        data = response.json()
        # HuggingFace peut renvoyer plusieurs formats diff√©rents selon le mod√®le
        if isinstance(data, list):
            text = data[0].get("generated_text", "")
        elif "generated_text" in data:
            text = data["generated_text"]
        else:
            text = str(data)

        # Nettoyage basique du texte
        text = text.split("assistant:")[-1].strip()
        return text or "üå´Ô∏è Le N√©ant murmure sans mot..."

    except Exception as e:
        print(f"[Erreur HF API] {e}")
        return "‚ö†Ô∏è Le narrateur se tait... (*erreur de connexion*)"
